/** Build a fast zip→city and city→zips lookup */
const ZIP_TO_CITY = new Map<string, string>();
const CITY_TO_ZIPS = new Map<string, string[]>();

for (const entry of CITY_ZIP_MAP) {
  const cityLower = entry.city.toLowerCase();
  CITY_TO_ZIPS.set(cityLower, entry.zips);
  for (const zip of entry.zips) {
    ZIP_TO_CITY.set(zip, entry.city);
  }
}

/**
 * Determines if a string looks like a zip code.
 * Accepts 5-digit US zips or 5+4 format.
 */
export function isZipCode(input: string): boolean {
  return /^\d{5}(-\d{4})?$/.test(input.trim());
}

/**
 * Given any location input (city name or zip code), returns a
 * deduplicated, lowercase-normalized set of all searchable tokens:
 * - the input itself (normalized)
 * - the canonical city name
 * - all zip codes for that city
 *
 * These tokens are stored on Helper.serviceAreas for indexing.
 */
export function expandLocation(input: string): string[] {
  const trimmed = input.trim();
  const tokens = new Set<string>();

  if (isZipCode(trimmed)) {
    // Input is a zip → find its city
    tokens.add(trimmed);
    const city = ZIP_TO_CITY.get(trimmed);
    if (city) {
      tokens.add(city);                        // "San Francisco"
      tokens.add(city.toLowerCase());          // "san francisco"
      const zips = CITY_TO_ZIPS.get(city.toLowerCase()) ?? [];
      zips.forEach(z => tokens.add(z));
    }
  } else {
    // Input is a city name → find its zips
    const lower = trimmed.toLowerCase();
    tokens.add(trimmed);                       // original casing
    tokens.add(lower);                         // lowercase
    const zips = CITY_TO_ZIPS.get(lower) ?? [];
    zips.forEach(z => tokens.add(z));
  }

  return Array.from(tokens);
}

/**
 * Expands an array of service areas (mixed cities and zips)
 * into one flat, deduplicated set of searchable tokens.
 * Call this at write time before saving to Firestore.
 */
export function expandServiceAreas(areas: string[]): string[] {
  const all = new Set<string>();
  for (const area of areas) {
    for (const token of expandLocation(area)) {
      all.add(token);
    }
  }
  return Array.from(all);
}

/**
 * Normalizes a search query from the user/AI into a canonical
 * token suitable for Firestore array-contains lookup.
 * - Zip stays as-is
 * - City name is returned with original casing (stored that way)
 */
export function normalizeSearchLocation(input: string): string {
  const trimmed = input.trim();
  if (isZipCode(trimmed)) return trimmed;
  // For city search, return the canonical casing if known
  const lower = trimmed.toLowerCase();
  const canonical = CITY_ZIP_MAP.find(e => e.city.toLowerCase() === lower);
  return canonical ? canonical.city : trimmed;
}
